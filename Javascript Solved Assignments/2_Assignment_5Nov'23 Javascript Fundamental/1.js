/*
Usage of Ternary Operator
->  A ternary operator is a shorthand way of writing an if-else statement. It allows a programmer to execute one block of code if a particular condition is true, and another block of code if the condition is false, all within a single line of code.

Syntax: `result = condition ? true expression : false expression;`;  
*/

let Num = 5;
console.log(Num%2 == 0 ? 'Num is even':'Num is odd');